
namespace IterationZero.Domain.Common
{
    /// <summary>
    /// Specifies the order in which the contents of a collection should be sorted.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// Indicates the contents should be sorted smallest to largest.
        /// </summary>
        Ascending = 0,

        /// <summary>
        /// Indicates the contents should be sorted largest to smallest.
        /// </summary>
        Descending = 2
    }
}